package review20211210;

//  comment라는 클래스 안에 main이라는 메소드가 있다.
public class CommentExam {
    public static void main(String[] args) { // 이렇게 문장 오른쪽으로 주석 처리도 가능하다.
        // 주석( comment )

        // 주석은 코드에 설명과 코드 상세 내용을 적을 때 사용한다.

        // 주석은 한 줄( 라인 주석 ) 주석과 여러 줄 주석( 블록 주석 ) 으로 나뉜다.

        // 한 줄( 라인 주석 )을 사용할 때 에는 //를 입력한다.

        // 여러 줄( 블록 주석 )을 사용할 때 에는 /**/를 입력한다

        // 한줄( 라인 주석 ) 사용 예

        /*
         * 
         * 여러 줄 주석( 블록 주석 ) 사용 예
         * 
         */

        System.out.println("Hello World");
        // Hello World라는 문구를 출력한다.
    }
}
